<p><strong>3. REGISTRO DE TICKETS DE COMPRA Y CÁLCULO DE BOTONES</strong></p>

<p>a) Registro de Tickets</p>

<p>Para registrar los tickets de compra, una vez que el participante tenga iniciada su sesión como usuario, deberá ingresar a la sección “Registro de Tickets” la cual lo dirigirá automáticamente a una pantalla donde transcribirá la información solicitada en los campos que aparecen en pantalla y que pertenecen al ticket de compra, que son: número de ticket y fecha de compra (la que aparece en el ticket de compra).</p>

<p>Si el proceso de registro de ticket de compra fue completado de manera exitosa se le notificará al participante en pantalla al momento de concluir.</p>

<p>b) Juego de destreza</p>
<ul>
  <li><p>El usuario podrá ganar participar por cada ticket de compra registrado de acuerdo a las siguientes bases:</p></li>
  <li><p>Al concluir correctamente el registro de cada ticket de compra, el usuario verá en la pantalla de su dispositivo una urna virtual transparente que contiene “Botones” en su interior. El juego de destreza consiste en que el usuario calcule sin pasarse de la cantidad y bajo sus propios métodos, el total de “Botones” contenidos al interior de la urna virtual.</p></li>
  <li><p>El usuario dispondrá de 2 minutos para ingresar la cantidad de “Botones” que según su cálculo contiene la urna virtual en el campo que tendrá visible en pantalla para tal fin. Si al transcurrir los 2 minutos el participante aún no ingresa la cantidad calculada, dispondrá de una prórroga de 2 minutos más para ingresar la cifra. De no hacerlo en este tiempo, se considerará como no completado el cálculo y el participante perderá el derecho a registrar ese ticket de compra. Para facilitar lo anterior, en pantalla aparecerá un cronómetro que indicará el tiempo transcurrido desde que se abre la pantalla para el cálculo hasta finalizar el límite de tiempo establecido.</p></li>
</ul>
<p>c) El usuario podrá comunicarse vía telefónica al número 01800 890 9859 en donde personal de atención al consumidor podrá ayudarlo en el proceso de registro de tickets y en las dudas relativas a la mecánica planteada.</p>

<p><strong>IX. INCENTIVOS</strong></p>

<p>A.  Minisplits de 1 tonelada. Los ganadores serán los 10 participantes que calculen el número exacto de “Botones” que caben en la urna virtual. En caso de que ningún participante acierte de manera exacta, se tomarán en cuenta los tickets de compra de los participantes que más se acerquen sin pasarse de la cantidad de “Botones” que caben en la urna virtual, al término de la vigencia de LA PROMOCIÓN y recibirán cada uno como incentivo un minisplit de 1 tonelada</p>

<p>B. Pantallas de TV de 32”. Del lugar 11 al lugar 25, los participantes con un cálculo del número exacto o más cercano sin pasarse de la cantidad de “Botones” contenidos en la urna virtual al término de la vigencia de LA PROMOCIÓN por debajo de los primeros 10 lugares, recibirán cada uno como incentivo una pantalla de TV de 32”</p>

<p>C. Monederos electrónicos con valor de $1,000.00 (Unmilpesos 00/100 M.N.). Del lugar 26 al lugar 35, los participantes con un cálculo del número exacto o más cercano sin pasarse de la cantidad de “Botones” contenidos en la urna virtual al término de la vigencia de LA PROMOCIÓN y por debajo del lugar 25, recibirán cada uno como incentivo un monedero electrónico con valor de $1,000.00 para canjearse en tiendas de autoservicio Casa Ley</p>



