<p>a) Cada participante se registrará ingresando los siguientes datos:</p>
<ul>
  <li><p>Nombre (s)</p></li>
  <li><p>Apellido (s)</p></li>
  <li><p>Correo electrónico (si el participante no cuenta con correo electrónico puede dejarse en blanco)</p></li>
  <li><p>Nombre de usuario</p></li>
  <li><p>Contraseña</p></li>
  <li><p>Fecha de nacimiento</p></li>
  <li><p>Estado</p></li>
  <li><p>Teléfono de localización celular</p></li>
  <li><p>Teléfono de localización fijo</p></li>
