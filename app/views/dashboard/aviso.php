<p>b) Todos los usuarios deberán leer y aceptar el Aviso de Privacidad y las Bases, Términos y Condiciones de LA PROMOCIÓN para finalizar satisfactoriamente su registro.</p>

<p>c) El usuario recibirá una confirmación de su registro en el correo electrónico ingresado, si cuenta con él, así como el de su nombre de usuario y contraseña. Si no cuenta con correo electrónico, puede llamar por teléfono para revisar su registro en el SITIO WEB. Es responsabilidad de los participantes proporcionar sus datos de forma correcta y completa.</p>

